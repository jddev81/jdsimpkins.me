<p>Milton Eats is a personal project being built on Laravel. The idea is to replicate a kind of "Grub Hub" application minus the ordering and delivering through the app.</p>

<p>It started out of frustration trying to find something to eat in my local area. I noticed a lot of the little mom and pop shops didn't have much of an online presence. I set out to build a directory to give each location's information and menu if available.</p>

